import java.util.Scanner;
public class NestedLoop {
    public static void main(String[] args){//repeat pattern 2 times
        for(int i=1;i<=2;i++){
            System.out.println(" Tiger");
            //print lion 2 times with space
            for(int j=2;j<=3;j++){
                System.out.println("   Lion");}
                //print cat 3 times
                for(int k=1;k<=3;k++){
                    System.out.println(" Cat");
                }
            }
        }
    }
    

